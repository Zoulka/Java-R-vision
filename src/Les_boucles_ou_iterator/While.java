package Les_boucles_ou_iterator;

public class While {
    public static void main(String[] args) {


/* Elle parcourt un bloc de code tant qu’une condition spécifiée est vraie,
 elle teste la condition avant d’exécuter le corps de la boucle Syntaxe de
 la boucle while

! Il est important de savoir que si la boucle l’expression reste vraie,
 le programme reste bloqué dans la boucle provoquant une boucle infinie
* */

int i= 0;
while(i<10){
    System.out.println("Le boucle commence de 0 à 9 " + i);
    i++;
    }
}}
